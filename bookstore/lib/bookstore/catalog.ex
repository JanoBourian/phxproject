  @doc """
  Returns the list of books.

  ## Examples

      iex> list_books()
      [%Book{}, ...]

  """
  def list_books do
    Repo.all(Book)
    |> Repo.preload([:authors, :categories])
  end

  @doc """
  Gets a single book.

  Raises `Ecto.NoResultsError` if the Book does not exist.

  ## Examples

      iex> get_book!(123)
      %Book{}

      iex> get_book!(456)
      ** (Ecto.NoResultsError)

  """
  def get_book!(id) do
    Book
    |> Repo.get!(id)
    |> Repo.preload([:authors, :categories])
  end

  @doc """
  Creates a book.

  ## Examples

      iex> create_book(%{field: value})
      {:ok, %Book{}}

      iex> create_book(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_book(attrs \\ %{}) do
    %Book{}
    |> change_book(attrs)
    |> Repo.insert()

  end

  @doc """
  Updates a book.

  ## Examples

      iex> update_book(book, %{field: new_value})
      {:ok, %Book{}}

      iex> update_book(book, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_book(%Book{} = book, attrs) do
    book
    |> change_book(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a book.

  ## Examples

      iex> delete_book(book)
      {:ok, %Book{}}

      iex> delete_book(book)
      {:error, %Ecto.Changeset{}}

  """
  def delete_book(%Book{} = book) do
    Repo.delete(book)
  end

  def change_book(%Book{} = book, attrs \\ %{}) do

    authors = get_authors_id(attrs["authors"])
    categories = get_categories_id(attrs["categories"])

    book
    |> Repo.preload([:categories, :authors])
    |> Book.changeset(attrs)
    |> Ecto.Changeset.put_assoc(:categories, categories)
    |> Ecto.Changeset.put_assoc(:authors, authors)
    |> validate_authors(authors)
    |> validate_categories(categories)

  end

  def get_authors_id(nil), do: []
  def get_authors_id(author_ids) do
    Repo.all(from a in Author, where: a.id in ^author_ids)
  end

  def get_categories_id(nil), do: []
  def get_categories_id(category_ids) do
    Repo.all(from c in Category, where: c.id in ^category_ids)
  end

  def validate_authors(changeset, authors) do
    with [] <- authors do
      changeset
      |> Ecto.Changeset.add_error(:authors, "authors have to have valid authors")
    else
      _ -> changeset
    end
  end

  def validate_categories(changeset, categories) do
    with [] <- categories do
      changeset
      |> Ecto.Changeset.add_error(:categories, "categories have to have valid categories")
    else
      _ -> changeset
    end
  end

end
